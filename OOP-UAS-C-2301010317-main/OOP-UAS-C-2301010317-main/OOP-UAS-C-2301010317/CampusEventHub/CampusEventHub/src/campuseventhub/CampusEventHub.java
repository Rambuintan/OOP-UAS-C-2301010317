    private void tampilData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("Nama Event");
        model.addColumn("Penyelenggara");
        model.addColumn("Tanggal");
        model.addColumn("Kategori");
        model.addColumn("Status");

        try {
            int no = 1;
            String sql = "SELECT * FROM tb_event";
            java.sql.Connection conn = koneksi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);

            while (res.next()) {
                model.addRow(new Object[]{
                    no++,
                    res.getString("nama_event"),
                    res.getString("penyelenggara"),
                    res.getDate("tanggal_event"),
                    res.getString("kategori"),
                    res.getString("status_event")
                });
            }
            tabel.setModel(model);
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal tampilkan data: " + e.getMessage());
        }
    }

    private void resetForm() {
        formNama.setText("");
        formPenyelenggara.setText("");
        formTanggal.setDate(null);
        formKategori.setSelectedIndex(0);
        formStatus.setSelectedIndex(0);
    }

