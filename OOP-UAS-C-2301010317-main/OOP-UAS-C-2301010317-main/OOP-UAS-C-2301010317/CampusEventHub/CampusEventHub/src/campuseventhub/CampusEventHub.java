    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        int row = tabel.getSelectedRow();

        // Ambil data dari tabel
        String nama = tabel.getValueAt(row, 1).toString();
        String penyelenggara = tabel.getValueAt(row, 2).toString();
        String tanggalStr = tabel.getValueAt(row, 3).toString(); // Format: yyyy-MM-dd
        String kategori = tabel.getValueAt(row, 4).toString();
        String status = tabel.getValueAt(row, 5).toString();

        // Set ke form input
        formNama.setText(nama);
        formPenyelenggara.setText(penyelenggara);
        formKategori.setSelectedItem(kategori);
        formStatus.setSelectedItem(status);

        try {
            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
            java.util.Date tanggal = sdf.parse(tanggalStr);
            formTanggal.setDate(tanggal);
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal parsing tanggal: " + e.getMessage());
        }
    }//GEN-LAST:event_tabelMouseClicked

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try {
            int row = tabel.getSelectedRow();
            String namaLama = tabel.getValueAt(row, 1).toString();

            String sql = "UPDATE tb_event SET nama_event=?, penyelenggara=?, tanggal_event=?, kategori=?, status_event=? WHERE nama_event=?";
            java.sql.Connection conn = koneksi.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, formNama.getText());
            pst.setString(2, formPenyelenggara.getText());
            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
            String tanggal = sdf.format(formTanggal.getDate());
            pst.setString(3, tanggal);
            pst.setString(4, formKategori.getSelectedItem().toString());
            pst.setString(5, formStatus.getSelectedItem().toString());
            pst.setString(6, namaLama);

            pst.executeUpdate();
            javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil diubah.");
            tampilData();
            resetForm();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal ubah data: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

